			<h2>Executive Summary</h2>
<p>This quality professional development program enables middle leaders across all schooling sectors in Queensland to gain exposure to and learn from high quality leadership practice, with a particular focus on the effective leadership of teaching and learning.</p>
<p>Designed by the Queensland Education Leadership Institute (QELi) in partnership with one of the world’s leading authorities on educational leadership - the prestigious National College for Teaching &amp; Leadership (NCTL) in the United Kingdom, MLP will be delivered by qualified and experienced facilitators from Australia.</p>
<p>The Middle Leadership Program is about developing highly effective middle leaders of teaching and learning to effectively close the gap in achievement and reduce variation in schools. The program promotes a culture of willingness to support others in their learning and to know how to do this, by providing expert input and coaching. </p>			<h2>Outcome</h2>
<p>You will develop key leadership capabilities that will enable you to:
	</p><ul>
		<li>Lead chage and improve the quality of teachig and learning through leadership</li>
		<li>Build a team to enhance performance and get the best from colleagues</li>
		<li>Enable change and sustained improvement in schools</li>
		<li>Use approaches to reduce variability and close gaps in achievement</li>
		<li>Learn with and from others, adopting an open mind set</li>
		<li>Support others in their learning</li>
		<li>Understand the realtionship between personal effectiveness and leadership development</li>
		<li>Lead a key initiative in their school</li>
	</ul>
<p></p>			<h2>Audience</h2>
